    private void issuebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issuebuttonActionPerformed
        // TODO add your handling code here:
        String ID,name,date;
        int nb,nbi = 0;
        ID=idtextfield.getText();
        name=t1.getText();
        date=t2.getText();
        nb=Integer.parseInt(noofbookstextfield.getText());
         try
        {
            Class.forName("java.sql.DriverManager");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swapnil","root","1234"); 
            Statement stmt = con.createStatement();
            String str="SELECT No_of_books_issued FROM LIBRARY WHERE ID='"+ID+"'";
            ResultSet rs=stmt.executeQuery(str);
             if(rs.next()==false)
             {
                 JOptionPane.showMessageDialog(this,"ERROR:Invalid ID");
             }
             else
             {
                if(rs.next())
                {
                    nbi=rs.getInt("No_of_books_issued");
                }
                if(nbi<=3 && (nbi+nb)<=3){
                //String query = "INSERT INTO Library VALUES('"+fname+"','"+lname+"','"+id+"','"+mob+"','" "','"+branch+"');" ;
                String query="INSERT INTO issue VALUES('"+ID+"','"+name+"','"+nb+"','"+date+"');"; 
                stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Books issued");
                idtextfield.setText("");
                noofbookstextfield.setText("");
                }
                else{
                    JOptionPane.showMessageDialog(this,"Number of Books Limit Exceeded.\n No of books issued already - "+nbi);
                }
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_issuebuttonActionPerformed

